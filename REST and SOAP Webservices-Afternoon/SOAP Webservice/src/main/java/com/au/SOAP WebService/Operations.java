package com.au.Soap;

public class Operations {
	
	public String add(int a, int b) {
		return String.valueOf(a + b);
	}
	
	public String sub(int a, int b) {
		return String.valueOf(a - b);
	}
	
	public String mul(int a, int b) {
		return String.valueOf(a * b);
	}
	
	public String div(int a, int b) {
		return String.valueOf(a / b);
	}

}
